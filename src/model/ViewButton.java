package model;

/**
 * @ClassName ViewButton
 * @Description 视图型菜单事件
 * @Author yanhuo
 * @Date 2018/11/8 8:56
 * @Version 1.0
 **/
public class ViewButton {
    private String type;
    private String name;
    private String url;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
